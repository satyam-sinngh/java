public class Logical {
    public static void main(String[] args) {
        System.out.println( (3>2) && (5>0) );  //And
        System.out.println( (3<2) && (5>0) );  //And
        System.out.println( (3<2) || (5>0) );  //Or
        System.out.println( (3<2) || (5<0) );  //Or
        System.out.println( !(3>2));   //Not


    }
}
